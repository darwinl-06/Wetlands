package model;

public class Wetland{
	
	private String name;
	private String ubication;
	private String type;
	private String url;
	private boolean protectedArea;
	private String km2;
	
	//Constructor
	
	public Wetland(String name, String ubication, String type, String url, boolean protectedArea, String km2){
		
		this.name = name;
		this.ubication = ubication;
		this.type = type;
		this.url = url;
		this.protectedArea = protectedArea;
		this.km2 = km2;
	}
	
	//Gets
	
	public String getName(){
		return name;
	}
	
	public String getUbication(){
		return ubication;
	}
	
	public String getType(){
		return type;
	}
	
	public String getUrl(){
		return url;
	}
	
	public boolean getProtectedArea(){
		return protectedArea;
	}
	
	public String getKm2(){
		return km2;
	}
	
	//Sets
	
	public void setName(String pName){
		name = pName;
	}
	
	public void setUbication(String pUbication){
		ubication = pUbication;
	}
	
	public void setType(String pType){
		type = pType;
	}
	
	public void setUrl(String pUrl){
		url = pUrl;
	}
	
	public void setProtectedArea(boolean pProtectedArea){
		protectedArea = pProtectedArea;
	}
	
	public void setKm2(String pKm2){
		km2 = pKm2;
	}
}